<script>
tmp=true;
var submit = function (response){  
    $("#loader-btn").show();
    $(".submit").hide();
    $.ajax({
        url: ROOT+'clients_nl/ajax_login',
        type: 'post',
        dataType: 'json',
        data: {
            "user_set":$('#user_set').is(":checked"),
            "email":$("#email").val(),
            "password":$("#password").val(),
            "setuju":$("#setuju").is(":checked"),
            "g-recaptcha-response":response
        }
    })
    .done(function(data) {
        if(data.is_error==1){ 
            grecaptcha.reset();
            alert_error(data.error_message);
            return; 
        }
        if($('#user_set').is(":checked") === true){
            window.location = "<?php echo base_url(DEFAULT_PAGE_USER); ?>";
        }else{
            window.location = "<?php echo base_url(DEFAULT_PAGE_ADVOKAT); ?>";
        }
        $("#loader-btn").hide();
        $(".submit").show();
    })
    .fail(function(data) {
        if(tmp){
            alert_error( "Server tidak merespon. Mohon cek koneksi internet anda. (Lakukan refresh jika dibutuhkan)\n" );
            grecaptcha.reset();
            tmp = false;
        }
        
        $("#loader-btn").hide();
        $(".submit").show();
        
    })
    .always(function() {
        
    }) ;
}
</script>
        
      